/*Select restaurants which serve all meals*/
SELECT tbl_RESTAURANT.R_NAME, tbl_RESTAURANT.R_RATING, tbl_RESTAURANT.R_GENRE, tbl_RESTAURANT.R_PRICE_RANGE, tbl_ADDRESS.A_CITY, tbl_HOURS.H_BREAKFAST, tbl_HOURS.H_LUNCH, tbl_HOURS.H_DINNER, tbl_HOURS.H_LATENIGHT
