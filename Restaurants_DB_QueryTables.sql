/*Select restaurants which serve breakfast*/
SELECT tbl_RESTAURANT.R_NAME, tbl_RESTAURANT.R_GENRE, tbl_RESTAURANT.R_PRICE_RANGE, tbl_HOURS.H_BREAKFAST, tbl_ADDRESS.A_CITY
FROM (tbl_RESTAURANT INNER JOIN tbl_ADDRESS ON tbl_RESTAURANT.R_RESTAURANT_ID = tbl_ADDRESS.R_RESTAURANT_ID) INNER JOIN tbl_HOURS ON tbl_RESTAURANT.R_RESTAURANT_ID = tbl_HOURS.R_RESTAURANT_ID
WHERE (((tbl_HOURS.H_BREAKFAST)=Yes))
ORDER BY tbl_RESTAURANT.R_PRICE_RANGE;

