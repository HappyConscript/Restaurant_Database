/*Select restaurants with higher ratings*/
SELECT tbl_RESTAURANT.R_NAME, tbl_RESTAURANT.R_GENRE, tbl_RESTAURANT.R_PRICE_RANGE, tbl_HOURS.H_DINNER, tbl_ADDRESS.A_CITY
FROM (tbl_RESTAURANT INNER JOIN tbl_ADDRESS ON tbl_RESTAURANT.R_RESTAURANT_ID = tbl_ADDRESS.R_RESTAURANT_ID) INNER JOIN tbl_HOURS ON tbl_RESTAURANT.R_RESTAURANT_ID = tbl_HOURS.R_RESTAURANT_ID
WHERE (((tbl_RESTAURANT.R_RATING)="4 Points"))
OR (((tbl_RESTAURANT.R_RATING)="5 Points"))
ORDER BY tbl_RESTAURANT.R_PRICE_RANGE;